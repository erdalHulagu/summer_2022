package summeR_odevLER;

public class Od_ForLoop1 {
    public static void main(String[] args) {
        int sonRakam=5;
        int ilkRakam=1;
        for (int i = ilkRakam; i <=sonRakam; i++) {
            if (i<sonRakam){
               System.out.print(i+",");
           }else{
                System.out.println(i);
            }

        }
    }
}
